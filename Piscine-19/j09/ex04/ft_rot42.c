char	*ft_rot42(char *str)
{
	int i;
	int temp;

	temp = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] >= 65 && str[i] <= 90)
		{
			temp = str[i];
			str[i] = (((temp - 65) + 42) % 26) + 65;
		}
		if (str[i] >= 97 && str[i] <= 122)
		{
			temp = str[i];
			str[i] = (((temp - 97) + 42) % 26) + 97;
		}
		i++;
	}
	return (str);
}
